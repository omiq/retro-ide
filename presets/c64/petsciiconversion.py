def convert_to_chr(c):
    """Convert POKE code to CHR$ code"""
    if c >= 0 and c <= 31:
        c+=64
    elif c >= 32 and c <= 63:
        return c 
    elif c >= 64 and c <= 93:
        c+=128
    elif c==94:
        c=255
    elif c==95:
        c=223
    elif c >= 96 and c <= 127:
        c+=64
    elif c >= 128 and c <= 159:
        c-=128
    elif c >= 160 and c <= 191:
        c-=128
    elif c >= 192 and c <= 223:
        c-=64
    elif c >= 224 and c <= 254:
        c-=64
    return c  # Return unchanged if out of range

def convert_to_poke(c):
    """Convert CHR$ code to POKE code"""
    return c  # Return unchanged if not found

